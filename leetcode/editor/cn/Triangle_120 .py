class Solution:
    def minimumTotal(self, triangle: List[List[int]]) -> int:
        all_zero = True
        for i in range(len(triangle)):
            for j in range(len(triangle[i])):
                if triangle[i][j] != 0:
                    all_zero = False
        if all_zero:
            return 0

        level = len(triangle)
        mem = [[None for j in range(i + 1)] for i in range(level)]
        l = []
        for i in range(level):
            for j in range(i + 1):
                tmp = self.miniToPos(triangle, i, j, mem)
                if i == level - 1:
                    l.append(tmp)

        return min(l)

    def miniToPos(self, triangle: List[List[int]], x: int, y: int, mem: List[List]) -> int:
        if x == 0:
            return triangle[0][0]
        if mem[x][y] is not None:
            return mem[x][y]

        if y == 0:
            ret = self.miniToPos(triangle, x - 1, y, mem) + triangle[x][y]
        elif y == len(triangle[x]) - 1:
            ret = self.miniToPos(triangle, x - 1, y - 1, mem) + triangle[x][y]
        else:
            left = self.miniToPos(triangle, x - 1, y - 1, mem) + triangle[x][y]
            right = self.miniToPos(triangle, x - 1, y, mem) + triangle[x][y]
            ret = min([left, right])
        mem[x][y] = ret
        return ret


