class Solution:
    def searchRange(self, nums: List[int], target: int) -> List[int]:
        s = 0
        e = len(nums) - 1
        last = -2
        m = -1
        while last != m and s <= e:
            last = m
            m = int((s + e) / 2)
            if nums[m] < target:
                s = m + 1
            elif nums[m] > target:
                e = m - 1
            else:
                s1 = m
                e1 = m
                while s1 - 1 >= 0 and nums[s1 - 1] == target:
                    s1 = s1 - 1
                while e1 + 1 < len(nums) and nums[e1 + 1] == target:
                    e1 = e1 + 1
                return [s1, e1]
        return [-1, -1]


