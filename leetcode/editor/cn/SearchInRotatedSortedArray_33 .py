class Solution:
    def search(self, nums: List[int], target: int) -> int:
        if len(nums) == 1:
            return 0 if nums[0] == target else -1
        r = self.findRotate(nums)
        s = 0
        e = len(nums) - 1
        if r >= 0:
            if nums[0] <= target:
                e = r
            elif nums[-1] >= target:
                s = r

        while s <= e:
            m = int((s + e) / 2)
            if e - s == 1:
                if nums[e] == target:
                    return e
                if nums[s] == target:
                    return s
                return -1
            if nums[m] < target:
                s = m + 1
            elif nums[m] > target:
                e = m - 1
            else:
                return m
        return -1

    def findRotate(self, nums: List[int]) -> int:
        if len(nums) == 1 or nums[0] < nums[-1]:
            return -1
        s = 0
        e = len(nums) - 1
        while s < e:
            m = int((s + e) / 2)

            if nums[m] > nums[s]:
                s = m
            elif nums[m] < nums[e]:
                e = m
            else:
                if e - s == 1:
                    return s

